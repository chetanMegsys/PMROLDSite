                                    <h2>Our Research Approach</h2>
                                    <div class="col-md-12 rt-card">
                                        <p>
                                            Today, the majority of business executives are concerned about competition from disruptive businesses. They fear the risk of being left behind. Outmanoeuvred by better-informed, faster-moving competitors.
                                        </p>
                                        <p>
                                            This is why market research has become important and vital in today’s business landscape: An essential tool for success.
                                        </p>
                                        <p>
                                            Market research as a service helps any company, service provider, individual or organisation make better, more informed decisions. The more research is embedded in the strategic plans of a business, the better equipped it is to deal with the changing environment with dynamic operations. It gives valuable insights that leverage the market position.
                                        </p>
                                        <p>
                                            We at Persistence Market Research adopt a robust research methodology that addresses the research aims, objectives, and research questions.
                                        </p>
                                        <p>
                                        The following questions help in understanding the research essentials.
                                        </p>
                                        <ul>
                                            <li>Why to conduct research (i.e. goals, objectives)</li>
                                            <li>What type of data to collect (e.g., qualitative or quantitative data)</li>
                                            <li>Where to focus (e.g. geography, demographics)</li>
                                            <li>Who to collect it from (i.e., the sampling strategy)</li>
                                            <li>When to complete (i.e. analysis, results)</li>
                                            <li>How to collect it (i.e., the data collection method)</li>
                                            <li>How to analyze it (i.e., the data analysis methods)</li>
                                        </ul>
                                        <img src="<?php echo WEBSITE_URL; ?>assets/images/pmr_approch_1.webp" alt="img of Research Approch - PMR" width="100%" height="auto" title="Research Approch - PMR">
                                        <p class="pt-4"><strong>Ready to Plan Your Research? Here are a few simple questions you need to answer.</strong></p>
                                        <p>
                                            If you're planning a market research project, don't forget to start with the WHY! (Sales loss, market share, new product testing, and more)
                                        </p>
                                        
                                        <p>
                                            Good project planning needs a clear grasp of the project's geography and fieldwork location, known as the "WHERE" component. (Effective project teams prioritize the WHERE and use it to drive project planning and execution)
                                        </p>
                                        <p>
                                            A project's initial phase involves defining the project's "WHO". This includes identifying all stakeholders and determining who will be responsible for project execution. (Keep all informed about project development, including challenges)
                                        </p>
                                        <p>
                                            The success of a project hinges on its proper execution and effective utilization of results. The “HOW” of a project is crucial to achieving these goals (A combination of methods - primary and secondary - will be used to access the desired information)
                                        </p>
                                        <p>
                                            The "WHAT" of a market research project is closely related to the "WHY" and "HOW" aspects of the project. (Recommendations require implementation consideration for product modifications and new product development)
                                        </p>
                                        <p>
                                        <strong>At Persistence Market Research, we follow a detailed research process:</strong>

                                        <img class="py-2" src="<?php echo WEBSITE_URL; ?>assets/images/pmr_research_process_1.webp" alt="img of Research Process - PMR" width="100%" height="auto" title="Research Process - PMR">
                                        </p>
                                        <ol>
                                            <li><strong>Define Problem/Objectives/Opportunity:</strong> At Stage 1, we set the right objectives once the problem statement is underscored. Our initial work sets the direction of the project by clearly defining the problem statement and setting the right dialogue with stakeholders. </li>
                                            <li><strong>Developing and Designing the Research Plan:</strong> At Stage 2, data gathering is conducted to perform a root-cause analysis on each query with a clear purpose. Additionally, clear data collation and feasibility studies enable us to determine the research scope, thereby informing the project timeline.</li>
                                            <li><strong>Designing Data Collection and Sampling Process:</strong> At Stage 3, once the research scope is set, we conduct a step-by-step primary research to correctly map your research needs with our findings. </li>
                                            <li><strong>Data Collection (Qualitative and Quantitative):</strong>  At Stage 4, data mining and data analytics techniques validate the studies and findings throughout the process. It helps us refine and retune the pace of the project.</li>
                                            <li><strong>Data Interpretation, Estimation, and Analysis:</strong> At Stage 5, estimation and statistical modelling are conducted in-house and run through various data models to add more accuracy.</li>
                                            <li><strong>Report Writing and Presentation:</strong> At Stage 6, rigorous data collection methods and validation studies are conducted, and the findings are compiled for final reporting. A detailed report compiled with facts, figures, and market studies is provided on the final day.</li>
                                        </ol>

